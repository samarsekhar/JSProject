// Strings
let person = "Pooja";
console.log(person.length);       // 5
console.log(person[0]);           // P
console.log(person[1]);           // o

let friend = "Anissa";
console.log(friend);       // Anissa


// Template literals
let boy1 = "Shivaa";
let boy2 = "Ram";
let sentence = `${boy2} is a frined of ${boy1}.`
console.log(sentence);          // output- Ram is a friend of Shivaa.


// Escape Sequence Characters 
let fruit = 'Bana\"na'
console.log(fruit);        // output - Bana"na
let fruits = "Bana\"na"
console.log(fruits);       // Both are same output

